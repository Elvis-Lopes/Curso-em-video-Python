frase = "Curso em Video Python"

# fatiando a frase
print(frase[3:13])

# fatiando e pulando letras
print(frase[1:15:2])

# contando letra 'o'
print(frase.count('o'))

# retornando tamanho da frase
print(len(frase))

# trocando trecho da string
print(frase.replace('Python', 'Android'))

# deixando a frase inteira em maiusculo
print(frase.upper())

# deixando a frase inteira em minusculo
print(frase.lower())

# encontrando a posição de uma palavra na frase
print(frase.find('Video'))

#dividindo a frase
print(frase.split())